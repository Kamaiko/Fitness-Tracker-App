/**
 * Database Types
 *
 * Type-safe interfaces matching SQLite schema
 * These will also match Supabase schema for easy sync
 */

// ============================================================================
// Core Tables
// ============================================================================

export interface User {
  id: string;
  email: string;
  preferred_unit: 'kg' | 'lbs';
  nutrition_phase: 'bulk' | 'cut' | 'maintenance';
  created_at: number;
  updated_at: number;
}

export interface Exercise {
  id: string;

  // ===== ExerciseDB fields (1:1 mapping) =====
  exercisedb_id: string; // Original ExerciseDB ID (e.g., "K6NnTv0")
  name: string;
  body_parts: string[]; // Anatomical regions: ["Chest", "Shoulders"]
  target_muscles: string[]; // Primary muscles: ["Pectoralis Major"]
  secondary_muscles: string[]; // Supporting muscles: ["Triceps", "Deltoids"]
  equipments: string[]; // Required equipment: ["Barbell", "Bench"]
  exercise_type: string; // "weight_reps" | "cardio" | "duration" | "stretching"
  instructions: string[]; // Step-by-step guide (array of strings)
  exercise_tips: string[]; // Safety and technique recommendations
  variations: string[]; // Alternative exercise versions
  overview?: string; // Descriptive summary
  image_url?: string; // Exercise image URL
  video_url?: string; // Exercise video URL
  keywords: string[]; // Search optimization terms

  created_at: number;
  updated_at: number;
}

export interface Workout {
  id: string;
  user_id: string;
  started_at: number; // Unix timestamp
  completed_at?: number;
  duration_seconds?: number;
  title?: string;
  notes?: string;
  nutrition_phase: 'bulk' | 'cut' | 'maintenance';
  // synced removed - internal WatermelonDB sync tracking
  created_at: number;
  updated_at: number;
}

export interface WorkoutExercise {
  id: string;
  workout_id: string;
  exercise_id: string;
  order_index: number; // 1, 2, 3...
  superset_group?: string; // 'A', 'B', etc.
  notes?: string;
  target_sets?: number;
  target_reps?: number;
  // synced removed - internal WatermelonDB sync tracking
  created_at: number;
  updated_at: number;
}

export interface ExerciseSet {
  id: string;
  workout_exercise_id: string;
  set_number: number;
  weight?: number;
  weight_unit?: 'kg' | 'lbs';
  reps?: number;
  duration_seconds?: number;
  distance_meters?: number;
  rpe?: number; // 1-10
  rir?: number; // 0-5
  rest_time_seconds?: number;
  completed_at?: number;
  notes?: string;
  is_warmup: boolean;
  is_failure: boolean;
  // synced removed - internal WatermelonDB sync tracking
  created_at: number;
  updated_at: number;
}

// ============================================================================
// Helper Types for Queries
// ============================================================================

/**
 * Full workout with exercises and sets
 * Used for display and sync
 */
export interface WorkoutWithDetails extends Workout {
  exercises: Array<WorkoutExerciseWithDetails>;
}

export interface WorkoutExerciseWithDetails extends WorkoutExercise {
  exercise: Exercise;
  sets: ExerciseSet[];
}

/**
 * Create types (without auto-generated fields)
 */
export type CreateWorkout = Omit<Workout, 'id' | 'created_at' | 'updated_at'> & {
  nutrition_phase: 'bulk' | 'cut' | 'maintenance';
};
export type CreateWorkoutExercise = Omit<WorkoutExercise, 'id' | 'created_at' | 'updated_at'>;
export type CreateExerciseSet = Omit<ExerciseSet, 'id' | 'created_at' | 'updated_at'>;

/**
 * Update types (optional fields only)
 */
export type UpdateWorkout = Partial<Omit<Workout, 'id' | 'user_id' | 'created_at'>>;
export type UpdateExerciseSet = Partial<
  Omit<ExerciseSet, 'id' | 'workout_exercise_id' | 'created_at'>
>;
